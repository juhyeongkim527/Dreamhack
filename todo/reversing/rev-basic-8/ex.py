target_list = [
    0xAC, 0xF3, 0x0C, 0x25, 0xA3, 0x10, 0xB7, 0x25, 0x16, 0xC6,
    0xB7, 0xBC, 0x07, 0x25, 0x02, 0xD5, 0xC6, 0x11, 0x07, 0xC5, 0x00
]

flag = ''

for target in target_list:
    for i in range(32, 127):
        if (-5 * i) & 0xff == target:
            flag += chr(i)
            break

print('flag : DH{'+flag+'}')
